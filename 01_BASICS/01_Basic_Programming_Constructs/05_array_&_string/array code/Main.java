public class Main {
    public static void main(String[] args) {
        // Creating an array
        int[] arr = {1, 2, 3, 4, 5};

        // Accessing elements of an array
        System.out.println(arr[0]);  // Output: 1

        // Modifying elements of an array
        arr[0] = 10;
        System.out.println(Arrays.toString(arr));  // Output: [10, 2, 3, 4, 5]
    }
}